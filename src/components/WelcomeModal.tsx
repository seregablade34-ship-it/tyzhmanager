// Склонение слова "день"
function daysWord(n: number): string {
  const abs = Math.abs(n) % 100
  const last = abs % 10
  if (abs >= 11 && abs <= 19) return 'дней'
  if (last === 1) return 'день'
  if (last >= 2 && last <= 4) return 'дня'
  return 'дней'
}

// Получить случайный элемент
function randomItem<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

// Ключи localStorage
const LS_LAST_VISIT = 'tyzhm_last_visit_date'

export default function WelcomeModal() {
  const [isOpen, setIsOpen] = useState(false)
  const [message, setMessage] = useState('')
  const [isComeback, setIsComeback] = useState(false)
  const [missedDays, setMissedDays] = useState(0)
  const [userName, setUserName] = useState('')

  useEffect(() => {
    const today = new Date().toISOString().split('T')[0]
    const lastVisit = localStorage.getItem(LS_LAST_VISIT)

    // Уже показывали сегодня — не показываем
    if (lastVisit === today) return

    // Загружаем имя пользователя из БД
    async function loadAndShow() {
      try {
        const settings = await db.userSettings.toCollection().first()
        if (settings?.userName) {
          setUserName(settings.userName)
        }
      } catch {
        // Если БД не доступна — просто без имени
      }

      // Считаем пропущенные дни
      let daysMissed = 0
      if (lastVisit) {
        const last = new Date(lastVisit)
        const now = new Date(today)
        const diffMs = now.getTime() - last.getTime()
        daysMissed = Math.floor(diffMs / (1000 * 60 * 60 * 24)) - 1
      }

      if (daysMissed > 0) {
        const phraseFn = randomItem(COMEBACK_PHRASES)
        setMessage(phraseFn(daysMissed))
        setIsComeback(true)
        setMissedDays(daysMissed)
      } else {
        setMessage(randomItem(GREETINGS))
        setIsComeback(false)
        setMissedDays(0)
      }

      setIsOpen(true)
      localStorage.setItem(LS_LAST_VISIT, today)
    }

    loadAndShow()
  }, [])

  if (!isOpen) return null

  // Формируем приветствие с именем
  const greetingText = isComeback
    ? `С возвращением${userName ? `, ${userName}` : ''}! ${missedDays} ${daysWord(missedDays)} друг без друга`
    : `👋 Приветствую${userName ? `, ${userName}` : ''}!`

  return (
    <div className="fixed inset-0 bg-black/50 z-50 flex items-center justify-center p-4">
      <div
        className={`bg-surface rounded-2xl shadow-2xl max-w-md w-full p-6 border-2 ${
          isComeback
            ? 'border-warning'
            : 'border-primary'
        }`}
      >
        {/* Заголовок — БЕЗ большого emoji */}
        <div className="text-center mb-4">
          <h2 className={`text-lg font-bold ${isComeback ? 'text-warning' : 'text-primary'}`}>
            {greetingText}
          </h2>
        </div>

        {/* Фраза */}
        <p className="text-text text-center text-base leading-relaxed mb-6">
          {message}
        </p>

        {/* Кнопка */}
        <button
          onClick={() => setIsOpen(false)}
          className={`w-full py-3 rounded-xl font-semibold text-white transition-colors cursor-pointer ${
            isComeback
              ? 'bg-warning hover:bg-warning/90'
              : 'bg-primary hover:bg-primary/90'
          }`}
        >
          {isComeback ? '🔥 Исправляюсь!' : '💪 Погнали!'}
        </button>
      </div>
    </div>
  )
}